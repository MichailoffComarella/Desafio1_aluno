    public NovoAluno(int IdAlt) {
        initComponents();
        this.IdAlt = IdAlt;
        
        try  {
            Connection con = DriverManager.getConnection("jbdc:mysql://localhost:3306/alunos");
            String db = "select * from alunos where idAluno = ?";
            PreparedStatement stmt = con.prepareStatement(db);
            stmt.setInt(1, IdAlt);
            ResultSet rs = stmt.executeQuery();
            
            while(rs.next()){
                txtID.setText(rs.getString("idAluno"));
                txtID.setEnabled(false);
                txtNome.setText(rs.getString("Nome"));
                txtIdade.setText(Integer.getInteger("Idade").toString());
            }
            
            stmt.close();
            con.close();
            
            JOptionPane.showMessageDialog(this, "Novo aluno cadastrado!");
            
            this.setClosable(true);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex);
        }
        
    }

