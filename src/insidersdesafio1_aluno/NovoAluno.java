    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        try {
            if (IdAlt == 0) {
                Connection con = DriverManager.getConnection("jbdc:mysql://localhost:3306/alunos");
                String db = "insert into alunos(Nome, Idade) values (?, ?)";
                PreparedStatement stmt = con.prepareStatement(db);
                stmt.setString(1, this.txtNome.getText());
                stmt.setInt(2, Integer.parseInt(this.txtIdade.getText()));
                stmt.execute();
                stmt.close();
                con.close();
                } else {
                Connection con = DriverManager.getConnection("jbdc:mysql://localhost:3306/alunos");
                String db = "update alunos set Nome=?, Idade=?";
                PreparedStatement stmt = con.prepareStatement(db);
                stmt.setString(1, this.txtNome.getText());
                stmt.setInt(2, Integer.parseInt(this.txtIdade.getText()));
                stmt.execute();
                stmt.close();
                con.close();
            }
            JOptionPane.showMessageDialog(this, "O novo aluno foi cadastrado com sucesso!");
            this.dispose();
        } catch (SQLException ex) {
                Logger.getLogger(NovoAluno.class.getName()).log(Level.SEVERE, null, ex);
            
        }
    }//GEN-LAST:event_btnSalvarActionPerformed


